						예약하신 인터뷰 날짜를 기준으로 통상 30일-45일 이내에 발급한 서류들이어야 하나, 30일 이내 발급을 권장하며, 재정서류만큼은 2주 내에 발급한 서류를 권장합니다. 모든 서류는 국문(원본)제출도 가능하나, 영문(원본)서류 발급을 권장합니다. (단, DS-160은 반드시 영문으로 작성). 서류는 가능한 모두 원본으로 준비해 주십시오. 비자 서류는 인터뷰 후 바로 모두 돌려받게 됩니다.
					</p>
					<p class="Style02_p">
						<strong>■ 미국 비자 심사의 결과</strong> :
						인터뷰 후 바로 그 자리에서 알 수 있으며, 대사관이 공지하는 미국 비자 수속기간은 5~8일 정도이나, 통상 2~3일 안에 미국 비자가 찍힌 여권이 집으로 배달 되며, 비자 업무량이 많아지는 여름철이나 연말에는 다소 지체될 수 있습니다.
