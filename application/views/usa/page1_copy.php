					<p class="Style02_p">
						<strong>■ 미국 비자 심사의 기본 가정</strong> :
						미국 이민법의 214조(b)항에 "모든 외국인은 비자신청 당시 자신이 비이민 지위를 누릴 권리가 있음을 심사관에게 납득시키기 전까지는 이민으로 간주된다."라고 명시되어 있습니다. 다시 말해, 미국 비자 심사를 받는 신청자가 깨닫지 못하더라도, 미국 비자 심사에서 영사는 모든 신청자에게 기본적으로 이민 의사가 있다고 가정하고, 제출하는 서류와 인터뷰 내용으로부터 이민 의사가 없음을 하나씩 반증해 가는 방식으로 심사가 이루어 집니다. 즉, 비이민비자 신청자는 사회적 기반, 가족, 직업, 경제 여건상 미국에 일정 기간 체류한 후 반드시 본국으로 돌아온다는 사실을 증명해야 합니다. 미국 대사관에서는 “신청자 본인에게 이민의사가 없으며, 본인의 나라에 안정적인 기반이 있다는 결정에 도움이 될 수 있는 재정서류를 반드시 제출하셔야 하는 것은 아닙니다.”라고 재정과 관련하여서는 여지를 주고 있습니다. 단, 유학비자와 문화교류비자 신청 시에는 반드시 처음 유학 체류기간 1년 동안의 학비와 생활비를 충당할 충분한 경제적 능력이 있음을 증명할 수 있는 재정근거 서류를 제출을 필수로 요구하고 있습니다. (참고로, 고등학교 교환학생 프로그램 신청자가 J1 비자를 받기 위해서는 신청자의 영어언어능력, 지적 능력 및 수학능력은 필수 조건입니다.)
					</p>
